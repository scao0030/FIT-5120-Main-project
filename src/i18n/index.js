export function t(lang, path) {
  const keys = path.split('.')
  let obj = translations[lang] ?? translations[DEFAULT_LANG]
  for (const k of keys) {
    obj = obj?.[k]
    if (obj === undefined) {
      // fallback to English
      let fallback = translations[DEFAULT_LANG]
      for (const fk of keys) fallback = fallback?.[fk]
      return fallback ?? path
    }
  }
  return obj
}